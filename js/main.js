let currentLang = 'az';

function applyLanguage(lang) {
    const t = translations[lang];
    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if (t[key] !== undefined) {
            if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.innerText = t[key];
            }
        }
    });
    const langBtn = document.getElementById('langBtn');
    if (langBtn) langBtn.innerText = lang.toUpperCase();
    currentLang = lang;
    localStorage.setItem('eco_lang_apple', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    // Language
    const savedLang = localStorage.getItem('eco_lang_apple') || 'az';
    applyLanguage(savedLang);

    const selector = document.getElementById('langSelector');
    const btn = document.getElementById('langBtn');
    if (btn && selector) {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            selector.classList.toggle('open');
        });
        document.querySelectorAll('.lang-dropdown button').forEach(button => {
            button.addEventListener('click', () => {
                const lang = button.getAttribute('data-lang');
                if (lang) applyLanguage(lang);
                selector.classList.remove('open');
            });
        });
        document.addEventListener('click', () => selector.classList.remove('open'));
    }

    // Scroll Reveal
    function reveal() {
        var reveals = document.querySelectorAll('.reveal');
        for (var i = 0; i < reveals.length; i++) {
            var windowHeight = window.innerHeight;
            var elementTop = reveals[i].getBoundingClientRect().top;
            var elementVisible = 100;
            if (elementTop < windowHeight - elementVisible) {
                reveals[i].classList.add('active');
            }
        }
    }
    window.addEventListener('scroll', reveal);
    reveal();

    // Dark Nav Check
    window.addEventListener('scroll', () => {
        const nav = document.querySelector('nav');
        const darkSection = document.querySelector('.dark-section');
        if(darkSection && nav) {
            const rect = darkSection.getBoundingClientRect();
            if(rect.top <= 54 && rect.bottom >= 0) {
                nav.classList.add('dark-mode');
            } else {
                nav.classList.remove('dark-mode');
            }
        }
    });

    // Mobile Menu
    const menuBtn = document.getElementById('menuBtn');
    const closeBtn = document.getElementById('closeMenu');
    const mobileOverlay = document.getElementById('mobileOverlay');
    if(menuBtn && closeBtn && mobileOverlay) {
        menuBtn.addEventListener('click', () => mobileOverlay.classList.add('open'));
        closeBtn.addEventListener('click', () => mobileOverlay.classList.remove('open'));
    }
});
